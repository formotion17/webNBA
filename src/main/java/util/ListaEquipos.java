	public static String findColorByAbreviatura(String abreviatura) {
        for (ListaEquipos equipo : ListaEquipos.values()) {
            if (equipo.getAbre().equals(abreviatura)) {
                return equipo.color;
            }
        }
        return "rgb(0, 0, 0)";
    }
	
